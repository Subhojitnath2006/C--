 #include <iostream>
using namespace std;
int main(){
    int x=4;
    cout<<x<<endl;
    x++;//post increament--> first print then increase.
    cout<<x<<endl;
    x--; //post decreament--> first print then decrease.
    cout<<x<<endl;
    ++x; //pre increament--> first increse then print.
    cout<<x<<endl;
    --x; //pre decreament--> first decrease then print.
    cout<<x<<endl;
}
